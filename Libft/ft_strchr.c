char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	t;

	i = 0;
	t = c;
	while (s[i] != t)
	{
		if (s[i] == '\0')
			return (0);
		i++;
	}
	return ((char *)&s[i]);
}
