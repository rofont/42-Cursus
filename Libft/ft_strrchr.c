char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (s[i] != c)
	{
		if (i == 0)
			return (0);
		i--;
	}
	return ((char *)s);
}
